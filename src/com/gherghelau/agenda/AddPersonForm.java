    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(this.txtEmail.getText().isEmpty() && 
           this.txtFirstName.getText().isEmpty() &&
           this.txtLastName.getText().isEmpty() &&
           this.txtPhone.getText().isEmpty()){
             JOptionPane.showMessageDialog(null, "Complete at least one field!");
             return;
        }
        
        Person toPass = vm.getPerson();
        int idToPass = toPass == null ? 0 : toPass.getId();
        
        boolean res = vm.addPersonToDb(idToPass,
                this.txtFirstName.getText(),
                this.txtLastName.getText(),
                this.picPath , 
                this.txtPhone.getText(),
                this.txtEmail.getText() ,
                this.vm.getForm().getOwnerOfAgenda().getId(),
                this.update);
        
        if(res){
            JOptionPane.showMessageDialog(null, "Successfully saved person!");
            if(!update){
                this.vm.getForm().getPersons().add(vm.getPerson());
            }
            this.vm.getForm().loadPersonsData();
            this.dispose();
            return;
        }
        JOptionPane.showMessageDialog(null, "Some error occured!");
        this.dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

