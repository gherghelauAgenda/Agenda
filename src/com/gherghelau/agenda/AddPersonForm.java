    public void makeBindings(){
        if(vm.getPerson() == null)
            return;
        
        Person p = vm.getPerson();
        this.txtFirstName.setText(p.getFirstname());
        this.txtLastName.setText(p.getLastname());
        this.txtPhone.setText(p.getTelephone());
        this.txtEmail.setText(p.getEmail());
        
        if(p.getPicturePath() != null)
            loadImage(p.getPicturePath());
    }
    
    private void loadImage(String absolutePath){
        try {
                BufferedImage myPicture = ImageIO.read(new File(absolutePath));
                Image dimg = myPicture.getScaledInstance(imgLabel.getWidth(), imgLabel.getHeight(),
                        Image.SCALE_SMOOTH);
                this.picPath = absolutePath;
                imgLabel.setIcon(new ImageIcon(dimg));
            }
         catch (IOException ex) {
             imgLabel.setText("Picture not found! Problems accessing file " + absolutePath);
             System.out.println("problem accessing file"+absolutePath);
         }
    }
    
