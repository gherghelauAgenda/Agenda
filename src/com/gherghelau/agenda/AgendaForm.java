    private void btnSaveProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveProfileActionPerformed

        if(this.details == null){
            int o_id = this.connection.getIdForNewOwnerDetails();
            o_id++;
            this.details = new OwnerDetails(o_id,this.owner);
        }
        
        this.details.setFirstName(this.txtFirstName.getText());
        this.details.setLastName(this.txtLastName.getText());
        this.details.setEmail(this.txtEmail.getText());

        boolean answer = this.connection.addOwnerDetails(details, !newDetails);

        if(answer){
            JOptionPane.showMessageDialog(null, "Details saved!");
        }
        else{
            JOptionPane.showMessageDialog(null, "Details couldn't been saved! Open the mySQL server or debug it!");
        }

    }//GEN-LAST:event_btnSaveProfileActionPerformed

