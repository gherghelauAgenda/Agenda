    public void loadPersonsData(){
        Vector<String> header = new Vector<String>();
        header.add("First Name");
        header.add("Last Name");
        header.add("Telephone");
        
        this.persons = connection.getPersonFromOwnerId(owner.getId());
        
        Vector<Vector<String>> model = new Vector<Vector<String>>();
        for(Person p : persons){
            Vector<String> properties = new Vector<String>();
            properties.add(p.getFirstname());
            properties.add(p.getLastname());
            properties.add(p.getTelephone());
            
            model.add(properties);
        }
        
        jTable1.setModel(new DefaultTableModel(model, header){ 
            @Override
            public boolean isCellEditable(int row, int column) {
               return false;
            }
        });
    }
    
    private void bindEventToTable() {
        // toDO : bind the events to the table
        Vector<String> header = new Vector<String>();
        header.add("Title");
        header.add("Date");
        
        Vector<Vector<String>> model = new Vector<Vector<String>>();
        
          for(AEvent e : events){
            Vector<String> properties = new Vector<String>();
            properties.add(e.getTitle());
            properties.add(e.getDate().toString());
            
            model.add(properties);
        }
        
        tblEvents.setModel(new DefaultTableModel(model, header){ 
            @Override
            public boolean isCellEditable(int row, int column) {
               return false;
            }
        });
    }
    
    public void loadEvents(){
        this.events = connection.getAllEventFor(owner);
        bindEventToTable();
    }
    
