    private void loadProfile(Owner own) {
          
        details = connection.getDetailsFrom(own);
        if(details == null)
        {
            System.out.println("Trist! detalii nule!");
            return;
        }
        // incarc field-urile
        this.txtFirstName.setText(details.getFirstName());
        this.txtLastName.setText(details.getLastName());
        this.txtEmail.setText(details.getEmail());
        
        this.newDetails = false;
        
        String absPath = details.getPicturePath();
        if(absPath != null){
            loadImage(absPath);
        }
    }
    
     private void loadImage(String absolutePath){
        try {
                BufferedImage myPicture = ImageIO.read(new File(absolutePath));
                Image dimg = myPicture.getScaledInstance(lblImageProfile.getWidth(), lblImageProfile.getHeight(),
                        Image.SCALE_SMOOTH);
                lblImageProfile.setIcon(new ImageIcon(dimg));
                lblImageProfile.setText("");
                this.details.setPicturePath(absolutePath);
        }
        catch (IOException ex) {
             lblImageProfile.setText("Picture not found! Problems accessing file ");
        }
    }
    
