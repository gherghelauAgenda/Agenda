    private AEvent getEventFromSelectedRow(){
        int rowSelected = tblEvents.getSelectedRow();
        if(rowSelected > -1)
            return events.elementAt(rowSelected);
        return null;
    }
    
    private Person getPersonFromSelectedRow(){
        int rowSelected = jTable1.getSelectedRow();
        if(rowSelected > -1)
            return persons.elementAt(rowSelected);
        return null;
    }
    
